
public class FootAnimals {
	 private String name;
     private String weight;
     private int age;
	
     
     public FootAnimals(String name, String weight, int age) {
 		super();
 		this.name = name;
 		this.weight = weight;
 		this.age = age;
 	}
 protected FootAnimals() {
	 
 }

	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getWeight() {
		return weight;
	}


	public void setWeight(String weight) {
		this.weight = weight;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}
     
     
}
